package main

import "fmt"

func main() {
	fmt.Println("Greater Than: (1 > 2)", 1 > 2)
	fmt.Println("Less Than: (1 < 2)", 1 < 2)
	fmt.Println("Less Than or Equal To: (1 <= 2)", 1 <= 2)
	fmt.Println("Greater Than or Equal To: (1 >= 2)", 1 >= 2)
	fmt.Println("Equivalent: (4.0 == 4)", 4.0 == 4)
	fmt.Println("Not Equivalent: (4.0 != 4.1)", 4.0 != 4.1)
}
